# [mondaiユーザガイド](<?php href('docs'); ?>) / mondaiについて

<img src="<?php direct('img/gijineko.svg'); ?>" style="width:200px;">

<img src="<?php direct('img/mondai.svg'); ?>" style="height:14px;">は苦手問題分析アプリケーションです。  
各設問ごとに過去の解答を一定数蓄積することで苦手問題を分析します。  
誰でもユーザ登録することができます。  
ユーザは自分のGitHubリポジトリから問題を追加できます。

<img src="<?php direct('img/mondai.svg'); ?>" style="height:14px;">の著作権は斎藤 健太にあります。  
出題された各問題は全てパブリックドメインになります。
